class Match {
    constructor(id, title, date, place, 
        team1, team2, 
        logo1, logo2, 
        score1, score2) {
        this.id = id;
        this.title = title;
        this.date = date;
        this.place = place;
        this.team1 = team1;
        this.team2 = team2;
        this.logo1 = logo1;
        this.logo2 = logo2;
        this.score1 = score1;
        this.score2 = score2;
    }
}

export default Match;